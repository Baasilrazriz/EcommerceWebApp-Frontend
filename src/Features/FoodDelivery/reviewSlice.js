const reviewSlice = createSlice({
    name: 'review',
    initialState,
    reducers: {
        countTotalReviews: (state) => {
            state.totalReviewsCount = state.reviews.length;
          },
        writeaReview:(state,action)=>{
        const name=action.payload.name;
        const desc=action.payload.desc;
        const star=action.payload.star?action.payload.star:0;
        const review= {
            name: name,
            desc:desc,
            star: star,
        }
        state.reviews.push(review);
      },
      calculateAverageRating: (state) => {
        const totalRating = state.reviews.reduce((acc, review) => acc + review.star, 0);
        const numberOfReviews = state.reviews.length;
        state.averageRating = numberOfReviews > 0 ? totalRating / numberOfReviews : 0.0;
      },
     
      openReviewModal: (state) => {
        state.isReviewModalOpen = true;  
      },
      sortByAscendingRating: (state) => {
        state.reviews.sort((a, b) => a.star - b.star);
      },
  
      sortByDescendingRating: (state) => {
        state.reviews.sort((a, b) => b.star - a.star);
      },
      closeReviewModal: (state) => {
        state.isReviewModalOpen = false;
      },
    },
  });
  
